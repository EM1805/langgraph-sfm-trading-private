from __future__ import annotations

import argparse
import csv
import json
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Dict, Iterable, List, Mapping, Optional

from .audit_log import DEFAULT_AUDIT_LOG_PATH, utc_now_iso
from .outcome_tracker import OutcomeTracker

DEFAULT_DATASET_OUT_DIR = "out/learning/datasets"


def _clean_str(value: Any, default: str = "") -> str:
    if value is None:
        return default
    text = str(value).strip()
    return text if text else default


def _clean_lower(value: Any, default: str = "unknown") -> str:
    return _clean_str(value, default).lower()


def _as_dict(value: Any) -> Dict[str, Any]:
    return dict(value) if isinstance(value, Mapping) else {}


def _as_list(value: Any) -> List[Any]:
    if value is None:
        return []
    if isinstance(value, list):
        return value
    if isinstance(value, tuple):
        return list(value)
    return [value]


def _bool_to_int(value: Any) -> int | str:
    if value is True:
        return 1
    if value is False:
        return 0
    return ""


def _float_or_blank(value: Any) -> float | str:
    if value is None or value == "":
        return ""
    try:
        return float(value)
    except (TypeError, ValueError):
        return ""


def _candidate_action_names(record: Mapping[str, Any]) -> List[str]:
    names: List[str] = []
    for item in _as_list(record.get("candidate_actions")):
        name = _clean_str(item)
        if name and name not in names:
            names.append(name)
    selected = _clean_str(record.get("selected_action"))
    if selected and selected not in names:
        names.insert(0, selected)
    return names


def _decision_payload(record: Mapping[str, Any]) -> Dict[str, Any]:
    metadata = _as_dict(record.get("metadata"))
    return _as_dict(metadata.get("decision_payload"))


def _evaluated_actions(record: Mapping[str, Any]) -> List[Dict[str, Any]]:
    payload = _decision_payload(record)
    return [dict(x) for x in _as_list(payload.get("evaluated_actions")) if isinstance(x, Mapping)]


def _selected_payload(record: Mapping[str, Any]) -> Dict[str, Any]:
    payload = _decision_payload(record)
    return _as_dict(payload.get("selected"))


def _risk_score(risk_level: str) -> int:
    return {
        "none": 0,
        "low": 1,
        "medium": 2,
        "unknown": 2,
        "high": 3,
        "critical": 4,
    }.get(_clean_lower(risk_level), 2)


def _ambiguity_score(ambiguity: str) -> int:
    return {
        "none": 0,
        "low": 1,
        "medium": 2,
        "unknown": 2,
        "high": 3,
        "very_high": 4,
        "critical": 4,
    }.get(_clean_lower(ambiguity), 2)


@dataclass
class DatasetBuildResult:
    """Summary of files generated by DatasetBuilder."""

    generated_at: str = field(default_factory=utc_now_iso)
    log_path: str = ""
    out_dir: str = ""
    learning_rows: int = 0
    rct_rows: int = 0
    finetuning_rows: int = 0
    files: Dict[str, str] = field(default_factory=dict)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "generated_at": self.generated_at,
            "log_path": self.log_path,
            "out_dir": self.out_dir,
            "learning_rows": self.learning_rows,
            "rct_rows": self.rct_rows,
            "finetuning_rows": self.finetuning_rows,
            "files": dict(self.files),
        }


class DatasetBuilder:
    """Build offline datasets from Amantia audit/outcome JSONL.

    This module is intentionally stdlib-only and does not estimate causal
    effects. It prepares clean, stable rows that later modules can consume:

    * ``learning_events``: one joined decision/outcome row per observed outcome.
    * ``rct_candidates``: low-risk observed decisions that may be useful for
      future A/B or RCT analysis.
    * ``finetuning_pairs``: preference-style examples where the chosen action is
      the Amantia-selected action and rejected actions are vetoed/lower-ranked
      alternatives from the same decision package.
    """

    def __init__(self, log_path: str | Path = DEFAULT_AUDIT_LOG_PATH) -> None:
        self.log_path = Path(log_path)
        self.tracker = OutcomeTracker(self.log_path)

    def _records(self) -> List[Dict[str, Any]]:
        return [record.to_dict() for record in self.tracker.build_records()]

    def build_learning_rows(self) -> List[Dict[str, Any]]:
        rows: List[Dict[str, Any]] = []
        for record in self._records():
            selected = _clean_str(record.get("selected_action"))
            candidates = _candidate_action_names(record)
            rows.append(
                {
                    "decision_event_id": _clean_str(record.get("decision_event_id")),
                    "outcome_event_id": _clean_str(record.get("outcome_event_id")),
                    "request_id": _clean_str(record.get("request_id")),
                    "timestamp": _clean_str(record.get("timestamp")),
                    "outcome_timestamp": _clean_str(record.get("outcome_timestamp")),
                    "user_message": _clean_str(record.get("user_message")),
                    "candidate_actions": "|".join(candidates),
                    "selected_action": selected,
                    "treatment_action": selected,
                    "gate_decision": _clean_str(record.get("gate_decision")),
                    "runtime_decision": _clean_str(record.get("runtime_decision")),
                    "veto": _bool_to_int(record.get("veto")),
                    "risk_level": _clean_lower(record.get("risk_level"), "unknown"),
                    "risk_score": _risk_score(record.get("risk_level")),
                    "ambiguity": _clean_lower(record.get("ambiguity"), "unknown"),
                    "ambiguity_score": _ambiguity_score(record.get("ambiguity")),
                    "evidence_tier": _clean_lower(record.get("evidence_tier"), "unknown"),
                    "identification_tier": _clean_lower(record.get("identification_tier"), "unknown"),
                    "confidence": _clean_lower(record.get("confidence"), "unknown"),
                    "reason_codes": "|".join(str(x) for x in _as_list(record.get("reason_codes"))),
                    "outcome": _clean_lower(record.get("outcome"), "unknown"),
                    "target_success": _bool_to_int(record.get("success")),
                    "target_harm": _bool_to_int(record.get("harm")),
                    "user_satisfaction": _float_or_blank(record.get("user_satisfaction")),
                    "latency_ms": _float_or_blank(record.get("latency_ms")),
                    "observed": 1,
                }
            )
        return rows

    def build_rct_candidate_rows(self) -> List[Dict[str, Any]]:
        rows: List[Dict[str, Any]] = []
        for row in self.build_learning_rows():
            gate_decision = _clean_lower(row.get("gate_decision"))
            risk_score = row.get("risk_score", 2)
            target_harm = row.get("target_harm")
            veto = row.get("veto")
            success_observed = row.get("target_success") != ""
            low_risk = isinstance(risk_score, int) and risk_score <= 2
            safe_decision = gate_decision in {"allow", "warn", "ask_clarification"}
            no_observed_harm = target_harm in {0, ""}
            eligible = bool(success_observed and low_risk and safe_decision and no_observed_harm and veto != 1)

            rct_row = dict(row)
            rct_row["eligible_for_rct"] = 1 if eligible else 0
            rct_row["rct_family"] = _clean_str(row.get("selected_action"))
            rct_row["rct_target"] = "target_success"
            rct_row["rct_exclusion_reason"] = "" if eligible else self._rct_exclusion_reason(row)
            rows.append(rct_row)
        return rows

    def _rct_exclusion_reason(self, row: Mapping[str, Any]) -> str:
        reasons: List[str] = []
        if row.get("target_success") == "":
            reasons.append("success_unobserved")
        if row.get("veto") == 1:
            reasons.append("vetoed")
        if row.get("target_harm") == 1:
            reasons.append("observed_harm")
        risk_score = row.get("risk_score", 2)
        if isinstance(risk_score, int) and risk_score > 2:
            reasons.append("risk_too_high")
        if _clean_lower(row.get("gate_decision")) not in {"allow", "warn", "ask_clarification"}:
            reasons.append("unsafe_gate_decision")
        return "|".join(reasons) or "not_eligible"

    def build_finetuning_rows(self) -> List[Dict[str, Any]]:
        rows: List[Dict[str, Any]] = []
        for record in self._records():
            selected_action = _clean_str(record.get("selected_action"))
            selected_payload = _selected_payload(record)
            evaluated = _evaluated_actions(record)
            rejected_actions: List[str] = []
            rejected_payloads: List[Dict[str, Any]] = []

            for decision in evaluated:
                action = _clean_str(decision.get("selected_action") or decision.get("candidate_action"))
                if not action or action == selected_action:
                    continue
                rejected_actions.append(action)
                rejected_payloads.append(
                    {
                        "action": action,
                        "decision": _clean_str(decision.get("decision")),
                        "runtime_decision": _clean_str(decision.get("runtime_decision")),
                        "reason": _clean_str(decision.get("reason")),
                    }
                )

            if not rejected_actions:
                candidates = _candidate_action_names(record)
                rejected_actions = [name for name in candidates if name != selected_action]

            rows.append(
                {
                    "input": _clean_str(record.get("user_message")),
                    "amantia_decision": _clean_str(record.get("gate_decision")),
                    "selected_action": selected_action,
                    "chosen_response": _clean_str(selected_payload.get("llm_instruction") or selected_payload.get("reason")),
                    "rejected_actions": rejected_actions,
                    "rejected_action_details": rejected_payloads,
                    "outcome": _clean_lower(record.get("outcome"), "unknown"),
                    "success": record.get("success"),
                    "harm": record.get("harm"),
                    "user_satisfaction": record.get("user_satisfaction"),
                    "metadata": {
                        "decision_event_id": _clean_str(record.get("decision_event_id")),
                        "outcome_event_id": _clean_str(record.get("outcome_event_id")),
                        "candidate_actions": _candidate_action_names(record),
                        "risk_level": _clean_lower(record.get("risk_level"), "unknown"),
                        "ambiguity": _clean_lower(record.get("ambiguity"), "unknown"),
                        "runtime_decision": _clean_str(record.get("runtime_decision")),
                        "reason_codes": list(record.get("reason_codes", []) or []),
                    },
                }
            )
        return rows

    def export_jsonl(self, rows: Iterable[Mapping[str, Any]], path: str | Path) -> int:
        out = Path(path)
        out.parent.mkdir(parents=True, exist_ok=True)
        count = 0
        with out.open("w", encoding="utf-8") as handle:
            for row in rows:
                handle.write(json.dumps(dict(row), ensure_ascii=False, sort_keys=True) + "\n")
                count += 1
        return count

    def export_csv(self, rows: Iterable[Mapping[str, Any]], path: str | Path) -> int:
        out = Path(path)
        out.parent.mkdir(parents=True, exist_ok=True)
        materialized = [dict(row) for row in rows]
        if not materialized:
            out.write_text("", encoding="utf-8")
            return 0
        fieldnames = list(materialized[0].keys())
        with out.open("w", encoding="utf-8", newline="") as handle:
            writer = csv.DictWriter(handle, fieldnames=fieldnames)
            writer.writeheader()
            for row in materialized:
                csv_row = dict(row)
                for key, value in list(csv_row.items()):
                    if isinstance(value, (list, dict)):
                        csv_row[key] = json.dumps(value, ensure_ascii=False, sort_keys=True)
                writer.writerow(csv_row)
        return len(materialized)

    def export_all(self, out_dir: str | Path = DEFAULT_DATASET_OUT_DIR) -> DatasetBuildResult:
        out = Path(out_dir)
        out.mkdir(parents=True, exist_ok=True)

        learning_rows = self.build_learning_rows()
        rct_rows = self.build_rct_candidate_rows()
        finetuning_rows = self.build_finetuning_rows()

        files = {
            "learning_events_jsonl": str(out / "learning_events.jsonl"),
            "learning_events_csv": str(out / "learning_events.csv"),
            "rct_candidates_jsonl": str(out / "rct_candidates.jsonl"),
            "rct_candidates_csv": str(out / "rct_candidates.csv"),
            "finetuning_pairs_jsonl": str(out / "finetuning_pairs.jsonl"),
            "summary_json": str(out / "dataset_summary.json"),
        }

        self.export_jsonl(learning_rows, files["learning_events_jsonl"])
        self.export_csv(learning_rows, files["learning_events_csv"])
        self.export_jsonl(rct_rows, files["rct_candidates_jsonl"])
        self.export_csv(rct_rows, files["rct_candidates_csv"])
        self.export_jsonl(finetuning_rows, files["finetuning_pairs_jsonl"])

        result = DatasetBuildResult(
            log_path=str(self.log_path),
            out_dir=str(out),
            learning_rows=len(learning_rows),
            rct_rows=len(rct_rows),
            finetuning_rows=len(finetuning_rows),
            files=files,
        )
        Path(files["summary_json"]).write_text(json.dumps(result.to_dict(), indent=2, ensure_ascii=False), encoding="utf-8")
        return result


def build_learning_dataset(path: str | Path = DEFAULT_AUDIT_LOG_PATH) -> List[Dict[str, Any]]:
    return DatasetBuilder(path).build_learning_rows()


def build_rct_dataset(path: str | Path = DEFAULT_AUDIT_LOG_PATH) -> List[Dict[str, Any]]:
    return DatasetBuilder(path).build_rct_candidate_rows()


def build_finetuning_dataset(path: str | Path = DEFAULT_AUDIT_LOG_PATH) -> List[Dict[str, Any]]:
    return DatasetBuilder(path).build_finetuning_rows()


def export_learning_datasets(
    path: str | Path = DEFAULT_AUDIT_LOG_PATH,
    out_dir: str | Path = DEFAULT_DATASET_OUT_DIR,
) -> Dict[str, Any]:
    return DatasetBuilder(path).export_all(out_dir).to_dict()


def main(argv: Optional[list[str]] = None) -> int:
    parser = argparse.ArgumentParser(description="Build Amantia offline learning datasets from audit/outcome JSONL.")
    parser.add_argument("--log", default=DEFAULT_AUDIT_LOG_PATH)
    parser.add_argument("--out-dir", default=DEFAULT_DATASET_OUT_DIR)
    args = parser.parse_args(argv)

    result = export_learning_datasets(args.log, args.out_dir)
    print(json.dumps(result, indent=2, ensure_ascii=False))
    return 0


